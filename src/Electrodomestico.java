public class Electrodomestico {
    private String numSerie;
    private String marca;
    private String modelo;
    private String tipoElectrodomestico;
    private String eficienciaEnergetica;
    private String precio;

    public String getNumSerie() {
        return numSerie;
    }

    public void setNumSerie(String numSerie) {
        this.numSerie = numSerie;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getTipoElectrodomestico() {
        return tipoElectrodomestico;
    }

    public void setTipoElectrodomestico(String tipoElectrodomestico) {
        this.tipoElectrodomestico = tipoElectrodomestico;
    }

    public String getEficienciaEnergetica() {
        return eficienciaEnergetica;
    }

    public void setEficienciaEnergetica(String eficienciaEnergetica) {
        this.eficienciaEnergetica = eficienciaEnergetica;
    }

    public String getPrecio() {
        return precio;
    }

    public void setPrecio(String precio) {
        this.precio = precio;
    }
}
